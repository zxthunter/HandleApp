package com.hunter.handleapp.bean;

import java.util.ArrayList;

/**
 * Created by zxt on 2016/2/24.
 */
public class InstBean {
    private Boolean success;
    private Boolean authorized;
    private ArrayList<Institution> institutions;


    public InstBean(Boolean success, Boolean authorized, ArrayList<Institution> institutions) {
        this.success = success;
        this.authorized = authorized;
        this.institutions = institutions;
    }

    public Boolean getSuccess() {
        return success;
    }

    public void setSuccess(Boolean success) {
        this.success = success;
    }

    public Boolean getAuthorized() {
        return authorized;
    }

    public void setAuthorized(Boolean authorized) {
        this.authorized = authorized;
    }

    public ArrayList<Institution> getInstitutions() {
        return institutions;
    }

    public void setInstitutions(ArrayList<Institution> institutions) {
        this.institutions = institutions;
    }
}
